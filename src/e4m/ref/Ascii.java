package e4m.ref;

public interface Ascii {

  static final int  NUL  =  0x000;  // NULL
  static final int  SOH  =  0x001;  // START OF HEADING
  static final int  STX  =  0x002;  // START OF TEXT
  static final int  ETX  =  0x003;  // END OF TEXT
  static final int  EOT  =  0x004;  // END OF TRANSMISSION
  static final int  ENQ  =  0x005;  // ENQUIRY
  static final int  ACK  =  0x006;  // ACKNOWLEDGE
  static final int  BEL  =  0x007;  // BELL
  static final int  BS   =  0x008;  // BACKSPACE
  static final int  HT   =  0x009;  // HORIZONTAL TABULATION
  static final int  LF   =  0x00A;  // LINE FEED
  static final int  VT   =  0x00B;  // VERTICAL TABULATION
  static final int  FF   =  0x00C;  // FORM FEED
  static final int  CR   =  0x00D;  // CARRIAGE RETURN
  static final int  SO   =  0x00E;  // SHIFT OUT
  static final int  SI   =  0x00F;  // SHIFT IN
  static final int  DLE  =  0x010;  // DATA LINK ESCAPE
  static final int  DC1  =  0x011;  // DEVICE CONTROL ONE
  static final int  DC2  =  0x012;  // DEVICE CONTROL TWO
  static final int  DC3  =  0x013;  // DEVICE CONTROL THREE
  static final int  DC4  =  0x014;  // DEVICE CONTROL FOUR
  static final int  NAK  =  0x015;  // NEGATIVE ACKNOWLEDGE
  static final int  SYN  =  0x016;  // SYNCHRONOUS IDLE
  static final int  ETB  =  0x017;  // END OF TRANSMISSION BLOCK
  static final int  CAN  =  0x018;  // CANCEL
  static final int  EM   =  0x019;  // END OF MEDIUM
  static final int  SUB  =  0x01A;  // SUBSTITUTE
  static final int  ESC  =  0x01B;  // ESCAPE
  static final int  FS   =  0x01C;  // FILE SEPARATOR
  static final int  GS   =  0x01D;  // GROUP SEPARATOR
  static final int  RS   =  0x01E;  // RECORD SEPARATOR
  static final int  US   =  0x01F;  // UNIT SEPARATOR

}
